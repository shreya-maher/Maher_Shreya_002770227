    private void DocAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DocAddActionPerformed
        // TODO add your handling code here:
        String hosp =  "";    
        try{
            
            Class.forName("com.mysql.jdbc.Driver");
            Connection docConnection = DriverManager.getConnection("jdbc:mysql://localhost/hospital","root", "");
            String DocSql = ("select doctorid from hospitals where doctorid = '"+DoctorID.getText()+"' ");
            PreparedStatement DocPreparedStatement = docConnection.prepareStatement(DocSql);            
            ResultSet commResultSet = DocPreparedStatement.executeQuery();
            while(commResultSet.next())
            {
            hosp = commResultSet.getString("doctorid");
            }
  
        }catch(Exception e){
        JOptionPane.showMessageDialog(null, e);
        }
        
        if(hosp == "" || DoctorID.getText().equals(""))  {
        JOptionPane.showMessageDialog(null,"Doctor ID doesnt exists in system or is blank, try correct ID/create Doctor profile first");
        DoctorID.requestFocus();
        return;
        }
        
         if(DocName.getText().equals("")) {
        JOptionPane.showMessageDialog(null,"Name Required");
        DocName.requestFocus();
        return;
        }
         
          if(DocSpecialisation.getText().equals("")) {
        JOptionPane.showMessageDialog(null,"Specialisation Required");
        DocSpecialisation.requestFocus();
        return;
        }
          
          if(DocCommunity.getText().equals("")) {
        JOptionPane.showMessageDialog(null,"Community Required");
        DocCommunity.requestFocus();
        return;
        }
           
        if(DocCity.getText().equals("")) {
        JOptionPane.showMessageDialog(null,"City Required");
        DocCity.requestFocus();
        return;
        }
        
        
        
        
        try{   
            
            Class.forName("com.mysql.jdbc.Driver");
            
            Connection DocAddConnection = DriverManager.getConnection("jdbc:mysql://localhost/hospital","root", "");
             Statement DocAddStatement = (Statement) DocAddConnection.createStatement();
     
             //part 1
            String Hospital = ("Select hospital from hospitaladmin where username = '"+HospitalAdminLogin.HospitalUsernameField.getText()+"' ");
            PreparedStatement HospitalPS = DocAddConnection.prepareStatement(Hospital);
            ResultSet HospitalResult = HospitalPS.executeQuery(); 
        
        while(HospitalResult.next()){
             Hospital =    HospitalResult.getString("hospital");        
          }
            
            
            String DocAddSql = "Insert into hospitals values (?,?,?,?,?,?,?)";
            PreparedStatement DocAddApp = DocAddConnection.prepareStatement(DocAddSql);
            
            DocAddApp.setString(1, DocCity.getText());
            DocAddApp.setString(2, DocCommunity.getText());           
            DocAddApp.setString(3, Hospital);
            DocAddApp.setString(4, DocName.getText());
            DocAddApp.setString(5,DocSpecialisation.getText());
            DocAddApp.setString(6,DoctorID.getText());
            DocAddApp.setString(7,HospitalAdminLogin.HospitalUsernameField.getText());
            DocAddApp.executeUpdate();
            
            JOptionPane.showMessageDialog(null,"Doctor Has Been Registered To Hospital");
            
            DoctorID.setText("");
             DocName.setText("");
             DocSpecialisation.setText("");
             DocCommunity.setText("");
             DocCity.setText("");

             
            DocAddConnection.close();

            
        }    catch(Exception e){
        JOptionPane.showMessageDialog(null, e);
   
    }
        
        
        
    }//GEN-LAST:event_DocAddActionPerformed

